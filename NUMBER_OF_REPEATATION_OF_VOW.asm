.MODEL SMALL
.STACK 100
.DATA 
STR1 DB 'RUET IS AN ENGINEERING UNIVERSITY$'
STR2 DB 'THERE IS NO VOWEL$'
STR3 DB 'THERE IS VOWEL$'
STR4 DB 'THE NUMBER OF VOWEL IS:$'
.CODE
MOV AX,@DATA
MOV DS,AX
LEA SI,STR1
MOV BL,0

FIND_VOWEL:
LODSB 
CMP AL,'$'
JE END_PROGRAM
CMP AL,'A'
JE VOWEL
CMP AL,'E'
JE VOWEL
CMP AL,'I'
JE VOWEL
CMP AL,'O'
JE VOWEL
CMP AL,'U'
JE VOWEL
JMP FIND_VOWEL

VOWEL:
INC BL
JMP FIND_VOWEL

END_PROGRAM:
CMP BL,0
JE NO_VOWEL_MESSAGE
JMP VOWEL_MESSAGE
HLT 

NO_VOWEL_MESSAGE:
MOV AX,@DATA
MOV DS,AX

MOV AH,09H
LEA DX,STR2
INT 21H
HLT

VOWEL_MESSAGE:
MOV AX,@DATA
MOV DS,AX

MOV AH,09H
LEA DX,STR3
INT 21H
CALL NEWL
MOV AH,09H
LEA DX,STR4
INT 21H

CMP BL,10
JB NOT_MORE_TEN 
MOV AX,0
MOV AL,BL
MOV BL,10
DIV BL
MOV BL,AL
MOV BH,AH
ADD BL,48D
ADD BH,48D
MOV DL,BL
MOV AH,02H
INT 21H
MOV DL,BH
MOV AH,02H
INT 21H

HLT

NOT_MORE_TEN:
ADD BL,48D
MOV DL,BL
MOV AH,02H
INT 21H
HLT

NEWL PROC       ;NEW LINE PROC IS CREATED FOR GENERATING NEW LINES WHERE NEEDED
    MOV AH, 2
    MOV DL, 0AH 
    INT 21H
    MOV AH, 2
    MOV DL, 0DH
    INT 21H
    RET
NEWL ENDP    
